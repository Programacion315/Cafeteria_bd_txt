    private String quitarPunto(double numero){
        
        String numeroString = numero + "";
        String nueva = "";
        for(int i = 0; i < numeroString.length(); i++){
            
            
            if(numeroString.charAt(i) == '.'){
                break;
            }
            nueva = nueva + numeroString.charAt(i) + "";
        }
        return nueva;
    }
    
