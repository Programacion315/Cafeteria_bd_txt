    private void guardarProductos(){
        File file = new File("Productos.txt");
        PrintWriter Escribe;
        if(!file.exists()){
            try {
                file.createNewFile();
            } catch (Exception e) { 
            }
        }
        try {
            Producto aux;
            Escribe = new PrintWriter(file, "utf-8");
            for (int i = 0; i < productos.size(); i++) {
                aux = productos.get(i);
                aux.guardar(Escribe);
            }
            Escribe.close();
        } catch (Exception e) {
        }
    }
    
